<script>
	$( document ).ready(function() {
    $(function () {
        $('#button').on('click', function (e) {
        	e.preventDefault();
        	var name = $('#name').val();
        	var password = $('#password').val();

            if (name == ""){
                $('#result').text("can't be without name");
            
            }else if (password == ""){
                $('#result').text("can't be without password");
            }else{
                 $.ajax({
            type: 'POST',
            url: 'core/login.php',
            data: {name: name, password: password},
            success: function (response) {
              $('#result').text(response);
							if (response == 'true') {
								window.location.replace("http://design-ceramic.epizy.com/public/php/index.php");
							}
            }
          });
            }

        });

      });

});
